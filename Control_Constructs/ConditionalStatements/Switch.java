package control_constructs;

public class Switch {

	public static void main(String[] args) {
		int x = 2;

		switch (x) {
		    case 1:
		        System.out.println("x is 1");
		        break;
		    case 2:
		        System.out.println("x is 2");
		        break;
		    case 3:
		        System.out.println("x is 3");
		        break;
		    default:
		        System.out.println("x is not 1, 2, or 3");
		        break;
		}

	}

}
