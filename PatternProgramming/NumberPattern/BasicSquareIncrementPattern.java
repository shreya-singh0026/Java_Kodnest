package com.kodnest.numberpattern;

public class BasicSquareIncrementPattern {

	public static void main(String[] args) {
		for(int i=1;i<5;i++) {
			for(int j=1;j<5;j++)
			{
				System.out.print(i);
			}
			System.out.println();

		}
	}

}
