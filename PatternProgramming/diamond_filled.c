#include<stdio.h>
int main(){
	int n=10;
	int i,j,k,l,i1,j1,k1,l1;
		for(i1=n;i1>=1;i1--){
		for(j1=1;j1<=i1;j1++) printf(" ");
		for(k1=1;k1<=2*(n-i1);k1++) printf("*");
		for(l1=1;l1<=i1;l1++) printf(" ");
		printf("\n");}
	for(i=1;i<=n;i++){
		for(j=1;j<=i;j++) printf(" ");
		for(k=1;k<=2*(n-i);k++) printf("*");
		for(l=1;l<=i;l++) printf(" ");
	printf("\n");	}
}

