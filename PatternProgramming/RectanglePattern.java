package com.kodnest.patterns.level4;

public class RectanglePattern {

	public static void main(String[] args) {
		for(int i=0;i<5;i++) {
			for(int j=0;j<5;j++) {
				System.out.print("*" + " ");
			}
			System.out.println();

		}
	}

}
